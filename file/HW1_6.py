a = float(input('請輸入購物金額(需多於8000元):'))

if a < 8000:
    print('輸入金額不足8000')
elif 18000>a>=8000:
    print('需支付:' + '%.1f'%(a*0.95) + '元')
elif 28000>a>=18000:
    print('需支付:'+ '%.1f'%(a*0.9) + '元')
elif 38000>a>=28000:
    print('需支付:' + '%.1f'%(a*0.8) + '元')
else:
    print('需支付:'+ '%.1f'%(a*0.7) + '元')